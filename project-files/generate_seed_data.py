# ── 추가 변형 생성 함수 ────────────────────────────────────────────────────────

def _make_variations(base_pairs: list[tuple[str, str]], multiplier: int = 2) -> list[tuple[str, str]]:
    """기존 쌍을 약간 변형해 데이터 다양성 증가."""
    question_prefixes = [
        "", "잠깐, ", "혹시 ", "그런데 ", "참고로 ", "솔직히 ",
    ]
    question_suffixes = [
        "", "?", " 알려줘.", " 궁금해.", " 확인해줄 수 있어?",
    ]
    variations = []
    for q, a in base_pairs:
        for _ in range(multiplier - 1):
            prefix = random.choice(question_prefixes)
            suffix = random.choice(question_suffixes)
            # 질문 끝의 ? 를 제거하고 suffix 적용
            q_mod = q.rstrip("?").rstrip(".")
            new_q = f"{prefix}{q_mod}{suffix}".strip()
            if len(new_q) >= 10:
                variations.append((new_q, a))
    return variations


# ── 메인 함수 ─────────────────────────────────────────────────────────────────

def _alpaca_from_pairs(pairs: list[tuple[str, str]]) -> list[dict]:
    """(질문, 답변) 쌍 → Alpaca 포맷 dict 리스트."""
    return [
        {
            "instruction": q,
            "input": "",
            "output": a,
            "system": DEFAULT_SYSTEM,
        }
        for q, a in pairs
    ]


def generate(
    count: int = 150,
    dry_run: bool = False,
    export: bool = False,
) -> list[dict]:
    """
    시드 데이터를 생성해 DB 또는 JSON으로 저장하고 결과 반환.

    DB 경로가 Windows 마운트 드라이브라 SQLite I/O 오류가 발생하면
    자동으로 Alpaca JSON 직접 저장 방식으로 전환합니다.

    Args:
        count:   생성할 최대 개수
        dry_run: True면 저장 없이 미리보기만
        export:  True면 생성 후 Alpaca JSON 추출까지 실행

    Returns:
        Alpaca 포맷 dict 리스트
    """
    # 원본 + 변형 합치기
    all_pairs = list(_SEED_PAIRS)
    all_pairs += _make_variations(_SEED_PAIRS, multiplier=3)
    random.shuffle(all_pairs)
    selected = all_pairs[:count]

    alpaca_samples = _alpaca_from_pairs(selected)

    if dry_run:
        print(json.dumps(alpaca_samples[:3], ensure_ascii=False, indent=2))
        print(f"\n[dry-run] 총 {len(alpaca_samples)}개 생성 (저장 없음)")
        return alpaca_samples

    # ── DB 저장 시도 ─────────────────────────────────────────
    saved = 0
    skipped = 0
    db_ok = False

    try:
        collector = DataCollector()
        print(f"[+] {len(selected)}개 시드 데이터 DB 저장 시작...")
        for q, a in selected:
            result = collector.log_interaction(
                question=q,
                answer=a,
                source="synthetic",
                context_used="[시드 데이터]",
                system_prompt=DEFAULT_SYSTEM,
            )
            if result:
                saved += 1
            else:
                skipped += 1
        print(f"[✓] DB 저장 완료: {saved}개 / 품질 미달 스킵: {skipped}개")
        db_ok = True

    except Exception as e:
        print(f"[!] DB 저장 실패 ({e.__class__.__name__}: {e})")
        print("    → Alpaca JSON 직접 저장 방식으로 전환합니다.\n")

    # ── JSON 직접 저장 (DB 실패 시 또는 export 옵션) ────────
    out_dir = Path(__file__).parent / "lora_data" / "processed"
    out_dir.mkdir(parents=True, exist_ok=True)

    if not db_ok or export:
        # train/val 분할 (90:10)
        random.shuffle(alpaca_samples)
        n_val = max(1, int(len(alpaca_samples) * 0.1))
        val_samples   = alpaca_samples[:n_val]
        train_samples = alpaca_samples[n_val:]

        train_path = out_dir / "alpaca_train.json"
        val_path   = out_dir / "alpaca_val.json"

        train_path.write_text(
            json.dumps(train_samples, ensure_ascii=False, indent=2),
            encoding="utf-8",
        )
        val_path.write_text(
            json.dumps(val_samples, ensure_ascii=False, indent=2),
            encoding="utf-8",
        )

        print(f"\n[✓] Alpaca JSON 저장 완료!")
        print(f"    train: {len(train_samples)}개 → {train_path}")
        print(f"    val:   {len(val_samples)}개   → {val_path}")
        print(f"\n  다음 단계:")
        print(f"  python lora_trainer.py --train {train_path} --val {val_path}")

    elif db_ok and not export:
        print("\n  Alpaca JSON 추출: python data_pipeline.py export")

    return alpaca_samples


def _run_export():
    """data_pipeline.py 의 run_pipeline() 을 직접 호출."""
